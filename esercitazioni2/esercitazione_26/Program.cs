int numero = 3;

numero++; // Incremento postfisso, numero diventa 4
Console.WriteLine($"Dopo incremento postfisso: {numero}"); 

++numero; // Incremento prefisso, numero diventa 5
Console.WriteLine($"Dopo incremento prefisso: {numero}"); 

numero--; // Decremento postfisso, numero diventa 4
Console.WriteLine($"Dopo decremento postfisso: {numero}"); 

--numero; // Decremento prefisso, numero diventa 3
Console.WriteLine($"Dopo decremento prefisso: {numero}"); 

numero++; // Incremento postfisso, numero diventa 4
Console.WriteLine($"Dopo secondo incremento postfisso: i Fantastici {numero}!");